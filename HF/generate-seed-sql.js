// scripts/generate-seed-sql.js

import { mkdirSync, writeFileSync } from "node:fs";
import { ALL_LESSONS } from "../lessons/all-lessons.js";

/**
 * Экранирует текст для SQL.
 * В SQLite одинарная кавычка экранируется двойной одинарной кавычкой.
 */
function sqlText(value) {
  return String(value ?? "").replace(/'/g, "''");
}

/**
 * Проверяем, что значение можно безопасно привести к числу.
 */
function toNumber(value, fieldName) {
  const numberValue = Number(value);

  if (!Number.isFinite(numberValue)) {
    throw new Error(`Поле ${fieldName} должно быть числом. Получено: ${value}`);
  }

  return numberValue;
}

/**
 * Первые 2 урока бесплатные.
 * Начиная с 3 урока — premium.
 */
function getIsPremium(lesson) {
  if (lesson.is_premium !== undefined && lesson.is_premium !== null) {
    return Number(lesson.is_premium);
  }

  return Number(lesson.order_num) <= 2 ? 0 : 1;
}

const lines = [];

lines.push("-- Auto-generated file. Do not edit manually.");
lines.push("-- Generated by scripts/generate-seed-sql.js");
lines.push("");
lines.push("PRAGMA foreign_keys = ON;");
lines.push("BEGIN TRANSACTION;");

lines.push(`
CREATE UNIQUE INDEX IF NOT EXISTS idx_lesson_steps_lesson_order
ON lesson_steps (lesson_id, order_num);
`);

for (const lesson of ALL_LESSONS) {
  const lessonId = toNumber(lesson.id, "lesson.id");
  const levelId = toNumber(lesson.level_id, "lesson.level_id");
  const orderNum = toNumber(lesson.order_num, "lesson.order_num");
  const isPremium = getIsPremium(lesson);

  if (!lesson.title) {
    throw new Error(`У урока id=${lessonId} не указан title`);
  }

  if (!Array.isArray(lesson.steps)) {
    throw new Error(`У урока id=${lessonId} steps должен быть массивом`);
  }

  lines.push(`
INSERT INTO lessons (
  id,
  level_id,
  order_num,
  title,
  description,
  is_premium
)
VALUES (
  ${lessonId},
  ${levelId},
  ${orderNum},
  '${sqlText(lesson.title)}',
  '${sqlText(lesson.description)}',
  ${isPremium}
)
ON CONFLICT(id) DO UPDATE SET
  level_id = excluded.level_id,
  order_num = excluded.order_num,
  title = excluded.title,
  description = excluded.description,
  is_premium = excluded.is_premium;
`);

  const stepOrders = lesson.steps.map((step) =>
    toNumber(step.order_num, `lesson ${lessonId} step.order_num`)
  );

  if (stepOrders.length > 0) {
    lines.push(`
DELETE FROM lesson_steps
WHERE lesson_id = ${lessonId}
AND order_num NOT IN (${stepOrders.join(", ")});
`);
  } else {
    lines.push(`
DELETE FROM lesson_steps
WHERE lesson_id = ${lessonId};
`);
  }

  for (const step of lesson.steps) {
    const stepOrderNum = toNumber(
      step.order_num,
      `lesson ${lessonId} step.order_num`
    );

    if (!step.step_type) {
      throw new Error(
        `У шага ${stepOrderNum} урока id=${lessonId} не указан step_type`
      );
    }

    if (!step.content || typeof step.content !== "object") {
      throw new Error(
        `У шага ${stepOrderNum} урока id=${lessonId} content должен быть объектом`
      );
    }

    const contentJson = JSON.stringify(step.content);

    lines.push(`
INSERT INTO lesson_steps (
  lesson_id,
  order_num,
  step_type,
  content_json
)
VALUES (
  ${lessonId},
  ${stepOrderNum},
  '${sqlText(step.step_type)}',
  '${sqlText(contentJson)}'
)
ON CONFLICT(lesson_id, order_num) DO UPDATE SET
  step_type = excluded.step_type,
  content_json = excluded.content_json;
`);
  }
}

lines.push("COMMIT;");
lines.push("");

mkdirSync("generated", { recursive: true });

writeFileSync("generated/seed-lessons.sql", lines.join("\n"), "utf8");

console.log("✅ SQL-файл создан: generated/seed-lessons.sql");
console.log(`✅ Уроков обработано: ${ALL_LESSONS.length}`);